    private void contabilizarMes(String data) {
        vendas = vendaController.getVendasMes(data);
        
        char[] dia;
        String diaMes;
        ArrayList<String> passados = new ArrayList<String>();
        passados.add("");
        
        if(vendas != null){
            for(Venda venda : vendas) {
                dia = venda.getData().toCharArray();
                diaMes = Character.toString(dia[0]) + Character.toString(dia[1]);
                if(!passados.contains(diaMes)){
                    Object[] dados = {diaMes, vendaController.getNumeroProdutosDia(diaMes + "/" + data), vendaController.getNumeroServicosDia(diaMes + "/" + data),
                    String.format("%.2f", vendaController.getValorDia(diaMes + "/" + data))};
                    tabelaMes.addRow(dados);
                    passados.add(diaMes);
                }
            }
        }
        passados = new ArrayList<String>();
        ArrayList<String> datasPagamento = new ArrayList<String>();
        if(cadernetas != null){
            for(Caderneta caderneta : cadernetas) {
                datasPagamento = caderneta.getTodasDataPagamentos();
                for(String s : datasPagamento){
                    dia = s.toCharArray();
                    diaMes = Character.toString(dia[0]) + Character.toString(dia[1]);
                    if(!passados.contains(diaMes) && s.contains(data)){
                        Object[] dados = {diaMes, cadernetaController.getValorDia(diaMes + "/" + data)};
                        tabelaCaderneta.addRow(dados);
                        passados.add(diaMes);
                    }
                }
            }
        }
        
        
        gerarTotal();
    }
    
    private void gerarTotal(){
        int tamanho = tabelaMes.getRowCount();
        float total = 0;
        if(tamanho > 0){
            for (int i=0; i<tamanho; i++){
                total = total + Float.parseFloat(tabelaMes.getValueAt(i, 3).toString().replace(",", "."));
            }
        }
        
        tamanho = tabelaCaderneta.getRowCount();
        if(tamanho > 0){
            for (int i=0; i<tamanho; i++){
                total = total + Float.parseFloat(tabelaCaderneta.getValueAt(i, 1).toString().replace(",", "."));
            }
        }
        jlTotal.setText(String.format("%.2f", total));
    }

