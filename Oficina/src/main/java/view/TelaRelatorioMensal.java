    private void buttonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBuscarActionPerformed
        
        String data = tfData.getText();
        if(!data.equals("")){
            if(data.matches("[0-9][0-9][/][0-9][0-9][0-9][0-9]")) {
                for(int i=0; i<tabelaMes.getRowCount(); i++){
                    tabelaMes.removeRow(0);
                }
                for(int i=0; i<tabelaCaderneta.getRowCount(); i++){
                    tabelaCaderneta.removeRow(0);
                }
                contabilizarMes(data);
            }
            else{
                JOptionPane.showMessageDialog(null, "Digite o mês e ano no formato MM/AAAA. Ex:(01/2020 equivale a janeiro de 2020)");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Digite o mês e ano que deseja buscar. Ex:(01/2020 equivale a janeiro de 2020)");
        }

    }//GEN-LAST:event_buttonBuscarActionPerformed

