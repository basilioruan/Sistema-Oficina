    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String tfPagamento = tfRecebimento.getText().replace(",", ".");
        if(!tfPagamento.equals("")){
            if(!tfPagamento.matches("[a-zA-Z]*") && !tfPagamento.matches("[!@#$%*()_+=-]*")){
                tfPagamento = tfPagamento.replace(",", ".");
                float recebimento = Float.parseFloat(tfPagamento);
                String cpf = caderneta.getCliente().getCpf();
                String data = caderneta.getDataAbertura();
                String dataPagamento = tfData.getText();
                float resto = Float.parseFloat(jlRestante.getText().replace(",", "."));
                try {
                    CadernetaController cadernetaController = new CadernetaController();
                    if(cadernetaController.receberPagamento(cpf, data, recebimento, resto, dataPagamento)){
                        JOptionPane.showMessageDialog(null, "Pagamento recebido");
                        dispose();
                        new TelaExibirCadernetas().setVisible(true);
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Erro ao receber pagamento");
                    }
                } catch (IOException ex) {
                    Logger.getLogger(TelaRecebimento.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(TelaRecebimento.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Digite somente números no campo de recebimento. Ex: 99,99");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "O recebimento não pode ser vazio");
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed



