    private void tfRecebimentoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfRecebimentoKeyPressed
        String tfPagamento = tfRecebimento.getText();
        
        if(!tfPagamento.equals("")){
            float pagamento = Float.parseFloat(tfPagamento);
            float restante = caderneta.getValorDevedor();
            float result = restante - pagamento;
            if(result >= 0){
                jlRestante.setText(String.format("%.2f", result));
                if(result == 0){
                    tfData.setEnabled(false);
                }
                else{
                    tfData.setEnabled(true);
                }
            }
            else{
                jlRestante.setText("0");
            }
        }
        else{
            jlRestante.setText(String.format("%.2f", caderneta.getValorDevedor()));
        }

    }//GEN-LAST:event_tfRecebimentoKeyPressed

